package Day14;

public class JavaForLoop {

    public static void main(String[] args) {
        // While loop
        int i = 0;
        while (i < 5) {
            System.out.println("i: " + i);
            i++;
        }
        System.out.println();

        // for loop
        for (int x = 0; x < 5; x++) {
            System.out.println("a: " + x);

        }
    }

}
