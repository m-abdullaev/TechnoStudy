package Day14;

public class Task03 {
    public static void main(String[] args) {

        for (int i = 0; i <= 20; i++) {
            if (i % 2 == 0) {
                System.out.println("Even: " + i);
            } else {
                System.out.println("odd: " + i);
            }

        }
    }
}
