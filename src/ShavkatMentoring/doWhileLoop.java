package ShavkatMentoring;

public class doWhileLoop {
    public static void main(String[] args) {
        int i = 0;

        do {
            System.out.println("Hello World");
            System.out.println(i);
            i++;
        } while (i < 10);

        System.out.println(i);
        System.out.println("code is finished");

    }
}

