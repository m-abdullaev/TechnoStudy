package ShavkatMentoring;

public class DoWhileTask1 {
    /*

        create one char and a

        print all the char from a - z

        in the while loop

         */
    public static void main(String[] args) {
        char a = 'a';
        while (a <= 'z') {
            System.out.println(a);
            a++;
        }

    }
}
