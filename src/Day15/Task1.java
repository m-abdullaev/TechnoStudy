package Day15;

public class Task1 {
    public static void main(String[] args) {
        // task
        // print grid of "#" with 10 rows and 10 columns
        // using nested loop
        for (int i = 0; i < 10; i++) {

            for (int j = 0; j < 10; j++) {
                System.out.print("#");

            }
            System.out.println();


        }

    }
}
